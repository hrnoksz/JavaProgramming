package day09_IfStatements;

public class MinimumNumber {

    public static void main(String[] args) {

        int n1 = 4;
        int n2 = 8;

        if(n1 < n2){
            System.out.println(n1 + " is minimum number");
        }else{
            System.out.println(n2 + " is minimum number");
        }
    }
}
