package day09_IfStatements;

public class MaximumNumber {

    public static void main(String[] args) {

        int num1 = 23;
        int num2 = 15;

        if(num1 > num2){
            System.out.println(num1 + " is maximum number");
        }else{
            System.out.println(num2 + " is maximum number");
        }
    }
}
