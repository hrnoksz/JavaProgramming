package day39_Recap;

public class MethodOverloading {

    private void method1(){

    }

    private int method1(int a){
        return a;
    }
    /*
    Any method (public, private, default, protected) in java always can be overloaded under these conditions;
    Method name has to be same, and parameters have to be different.
    Return type can be same or different.
     */
}
